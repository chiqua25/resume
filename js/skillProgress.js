if(document.getElementsByClassName('progress-0').length != 0) {
    var circle0 = new ProgressBar.Circle('.progress-0', {
        color: '#c03423',
        strokeWidth: 15,
        trailColor: '#212c38',
        trailWidth: 15,
        duration: 1500,
        text: {
            value: '0'
        },
        step: function(state, bar) {
        bar.setText((bar.value() * 100).toFixed(0) + '%');
        }
    });

    circle0.animate(0.0);  // Number from 0.0 to 1.0
};

